package bto.Enums;

/**
 * Represents the marital status of an individual in the system.
 * <p>
 * This enum defines the possible marital statuses that can be assigned
 * to users or applicants within the application.
 */
public enum MaritalStatus {
	/** Indicates that the individual is not married. */
	SINGLE,

	/** Indicates that the individual is legally married. */
	MARRIED
}